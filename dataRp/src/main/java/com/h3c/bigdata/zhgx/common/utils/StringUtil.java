    /**
     * 使特定位置的英文字母大写
     */
    public static String upperSpec(@NotNull String str, int index) {
        if (index < 0 || index > str.length()) {
            throw new IllegalArgumentException("无效的参数 " + index);
        }
        char[] v = str.toCharArray();
        char s = v[index];
        if (s > 96 && s < 123) {
            v[index] = (char) (s - 32);
        }
        return new String(v);
    }

    /**
     * 对字符串中的部分特殊字符进行转意
     * 转意的字符包括：\ + - ! ( ) : ^ [ ] " { } ~  /
     * @param s 转意字符串
     * @return 转意后字符串
     */
    public static String escape(String s) {
        StringBuilder sb = new StringBuilder();
        for(int i = 0; i < s.length(); ++i) {
            char c = s.charAt(i);
            if(c == 92 || c == 43 || c == 33 || c == 33 || c == 94 || c == 91 || c == 93 || c == 123 || c == 125 || c == 126 || c == 47) {
                sb.append('\\');
            }
            sb.append(c);
        }
        return sb.toString();
    }

